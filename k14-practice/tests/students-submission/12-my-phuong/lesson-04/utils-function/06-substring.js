// File utils-function/06-substring.js
// Data:
const fullName = "Nguyễn Văn A";
const date = "2024-05-19";
const email = "example@gmail.com ";

// Trich xuat ho tu fullName (tu dau den ky tu thu 6)
console.log("Trich xuat ho tu fullName (tu dau den ky tu thu 6): ", fullName.substring(0, 6));

// Trich xuat nam tu date
console.log("Trich xuat nam tu date: ", date.substring(0, 4));

// Trich xuat ten mien tu email (tu sau @)
console.log("Trich xuat ten mien tu email: ", email.substring(8));